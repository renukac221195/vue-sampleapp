<template>
  <v-card>
    <v-container fluid style="min-height: 0; max-width: 1000px" grid-list-lg>
      <v-layout column xs12>
        <v-layout row wrap xs12 offset-sm3>
          <v-flex xs2>
            <v-card class="my-3">
              <v-toolbar color="teal" dark>
                <v-toolbar-title>Feeds</v-toolbar-title>
              </v-toolbar>

              <v-list subheader v-for="type in types" :key="type">
                <v-list-tile @click="changeType(type)">
                  <v-list-tile-content>
                    <v-list-tile-title>{{type}}</v-list-tile-title>
                  </v-list-tile-content>
                </v-list-tile>
              </v-list>
            </v-card>
          </v-flex>
          <v-flex xs10>
            <v-card class="my-3" v-for="feed in feeds" :key="feed.id">
              <v-card-title>
                <div>
                  <span class="grey--text">
                    <v-avatar size="36px">
                      <img :src="feed.dummyimage" :alt="feed.name">
                    </v-avatar>
                  </span>
                  <span>
                    <strong>{{ feed.type }}</strong> uploaded a photo.
                  </span>
                  <br>
                </div>
              </v-card-title>
              <v-card-title>{{feed.title}}</v-card-title>
              <v-card-media class="white--text" height="500px">
                <img :src="feed.image" :alt="feed.image">
                <v-container fill-height fluid>
                  <v-layout fill-height>
                    <v-flex xs12 align-end flexbox></v-flex>
                  </v-layout>
                </v-container>
              </v-card-media>
              <v-card-actions>
                <p>{{ feed.description }}</p>
              </v-card-actions>
              <v-divider></v-divider>
              <v-card-actions>
                <v-btn flat color="blue">Like</v-btn>
                <v-btn flat color="blue">Comment</v-btn>
              </v-card-actions>
            </v-card>
          </v-flex>
        </v-layout>
      </v-layout>
    </v-container>
  </v-card>
</template>

<script>
export default {
  data() {
    return {
      feedType: "All",
      types: []
    };
  },
  created() {
    if (this.$store.getters.getCurrentUserData) {
      this.$store
        .dispatch("fetchAllUserFeeds", "/static/Feeds.json")
        .then(response => {
          this.types = [...new Set(response.map(item => item.type))];
        });
    } else {
      this.$router.push("/");
    }
  },
  computed: {
    feeds() {
      if (this.feedType === "All") {
        return this.$store.getters.allUserFeeds;
      } else {
        return this.$store.getters.allUserFeeds.filter(
          f => f.type === this.feedType
        );
      }
    },
    getImageUrl(img) {
      return require(`${img}`);
    }
    // getUserInfo(feed){
    //   From store userlist find userId of feed
    //   create object {
    //     dp:
    //     f name
    //     l name
    //   }
    // }
  },
  methods: {
    changeType(type) {
      this.feedType = type;
    }
  }
};
</script>
